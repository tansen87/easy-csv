export function getFunctionByName(name: string): XanFunction | undefined {
  return xanFunctions.find((f) => f.name === name);
}

export function getFunctionsByCategory(category: XanFunction["category"]): XanFunction[] {
  return xanFunctions.filter((f) => f.category === category);
}

export function searchFunctions(query: string): XanFunction[] {
  const lower = query.toLowerCase();
  return xanFunctions.filter(
    (f) =>
      f.name.toLowerCase().includes(lower) ||
      f.description.toLowerCase().includes(lower)
  );
}
